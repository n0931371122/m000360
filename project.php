                                <p>同為音樂工作者的男女主人，形容彼此就像鋼琴的黑鍵與白鍵般，音調不同，但在一首曲子中卻同等重要。溝通的過程中，我們發現屋主夫妻的相處模式，表面理性沈著，但心中卻有著音樂人的浪漫彭拜。</p>
